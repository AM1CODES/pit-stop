import type { NextPage } from 'next';
import Head from 'next/head';
import { useState } from 'react';
import { ethers } from 'ethers';
import Web3Modal from 'web3modal';
import WalletConnectProvider from '@walletconnect/web3-provider';
import { useDispatch } from 'react-redux';
import { authSuccess } from '../store/auth/actions';

const Home: NextPage = () => {
  const [account, setAccount] = useState('');
  const [connection, setConnection] = useState(false);
  const [loggedIn, setLoggedIn] = useState(false);

  const dispatch = useDispatch();

  async function getWeb3Modal() {
    const web3Modal = new Web3Modal({
      network: 'mainnet',
      cacheProvider: false,
      providerOptions: {
        walletconnect: {
          package: WalletConnectProvider,
          options: {
            infuraId: process.env.NEXT_PUBLIC_INFURA_ID
          }
        }
      }
    });
    return web3Modal;
  }

  async function connect() {
    const web3Modal = await getWeb3Modal();
    const connection = await web3Modal.connect();
    const provider = new ethers.providers.Web3Provider(connection);
    const accounts = await provider.listAccounts();
    setConnection(connection);
    setAccount(accounts[0]);
  }

  async function signIn() {
    const authData = await fetch(`/api/auth?address=${account}`);
    const user = await authData.json();
    console.log(user.nonce);
    // @ts-ignore
    const provider = new ethers.providers.Web3Provider(connection);
    const signer = provider.getSigner();
    const signature = await signer.signMessage(user.nonce.toString());
    const response = await fetch(
      `/api/verify?address=${account}&signature=${signature}`
    );
    const data = await response.json();
    setLoggedIn(data.authenticated);
    console.log(loggedIn);
    dispatch(authSuccess(account));
  }

  return (
    <div className="text-center text-red-700">
      <Head>
        <title>Pit Stop</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {!connection && (
        <button className="bg-white text-black p-5 mx-auto" onClick={connect}>
          {' '}
          Connect Wallet
        </button>
      )}
      {connection && !loggedIn && (
        <div>
          <button className="bg-white text-black p-5 mx-auto" onClick={signIn}>
            Sign In
          </button>
        </div>
      )}
      {loggedIn && (
        <h1 className="text-xl font-semibold text-center text-white mx-auto">
          Welcome, {account}
        </h1>
      )}
    </div>
  );
};

export default Home;
